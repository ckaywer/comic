

export function focused_text_color(focused:boolean)
{
    if(focused)
    {
        return "#FFA300";
    }
    else
    {
        return "#9B9C9C";
    }
}

export function focused_image_color(focused:boolean)
{
    if(focused)
    {
        return "#59D568";
    }
    else
    {
        return "#9B9C9C";
    }
}